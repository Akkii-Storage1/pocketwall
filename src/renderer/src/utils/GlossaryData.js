/**
 * Get all categories
 * @returns {Array} - Unique categories
 */
export function getAllCategories() {
    return [...new Set(GLOSSARY_TERMS.map(item => item.category))].sort();
}

/**
 * Get terms by category
 * @param {string} category - Category name
 * @returns {Array} - Terms in that category
 */
export function getTermsByCategory(category) {
    return GLOSSARY_TERMS.filter(item => item.category === category);
}

/**
 * Search terms
 * @param {string} query - Search query
 * @returns {Array} - Matching terms
 */
export function searchTerms(query) {
    const lowerQuery = query.toLowerCase();
    return GLOSSARY_TERMS.filter(item =>
        item.term.toLowerCase().includes(lowerQuery) ||
        item.definition.toLowerCase().includes(lowerQuery)
    );
}

export default {
    GLOSSARY_TERMS,
    getAllCategories,
    getTermsByCategory,
    searchTerms
};
