/**
 * Get tip of the day based on current date
 * @returns {object} - Tip object
 */
export function getTipOfTheDay() {
    const dayOfYear = Math.floor((Date.now() - new Date(new Date().getFullYear(), 0, 0)) / 1000 / 60 / 60 / 24);
    const index = dayOfYear % FINANCIAL_TIPS.length;
    return FINANCIAL_TIPS[index];
}

/**
 * Get random tip
 * @returns {object} - Tip object
 */
export function getRandomTip() {
    return FINANCIAL_TIPS[Math.floor(Math.random() * FINANCIAL_TIPS.length)];
}

/**
 * Get tips by category
 * @param {string} category - Category name
 * @returns {Array} - Array of tips
 */
export function getTipsByCategory(category) {
    return FINANCIAL_TIPS.filter(tip => tip.category === category);
}

/**
 * Get all categories
 * @returns {Array} - Array of unique categories
 */
export function getAllCategories() {
    return [...new Set(FINANCIAL_TIPS.map(tip => tip.category))];
}

